#####  math  #####
import math

# special constants
print('pi:', math.pi)
print('e:', math.e)

# converting to integers
x = math.pi
print('regular rounding', round(x), '; type:', type(round(x)))
print('truncation:', math.trunc(x), '; type:', type(math.trunc(x)))
print('floor:', math.floor(x), '; type:', type(math.floor(x)))
print('ceiling:', math.ceil(x), '; type:', type(math.ceil(x)))
